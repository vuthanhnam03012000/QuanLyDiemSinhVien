    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        int row = studenttable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(FindStudentFrame.this, "Vui Long Chon Sinh Vien", "Loi", JOptionPane.ERROR_MESSAGE);

        } else {

            int confirm = JOptionPane.showConfirmDialog(FindStudentFrame.this, "Ban Chac Chan Muon Xoa?");

            if (confirm == JOptionPane.YES_OPTION) {
                int idStudent = Integer.valueOf(String.valueOf(studenttable.getValueAt(row, 0)));
                studentController.deleteStudent(idStudent);
                defaultTableModel.setRowCount(0);
                setdata(studentController.getAllStudent());
            }

        }

    }//GEN-LAST:event_btndeleteActionPerformed

