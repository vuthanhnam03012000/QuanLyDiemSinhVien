    public void updateThongtinFind() {

        student.setId(String.valueOf(id.getText()));
        student.setName(name.getText());
        student.setGender(gender.getText());
        student.setDate(date.getText());
        student.setLop(lop.getText());
        student.setAddress(address.getText());

        studentController.updateStudent(student);

        for (int i = 0; i <= studenttable.getRowCount(); i++) {
            if (studenttable.getValueAt(i, 0).equals(Integer.parseInt(id.getText()))) {
                studenttable.setValueAt(name.getText(), i, 1);
                break;
            }
        }
        JOptionPane.showMessageDialog(null, "Cập nhật thành thành công!");
    }

