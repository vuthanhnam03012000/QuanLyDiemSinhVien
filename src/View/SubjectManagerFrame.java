    public void SelectedRow() {

        int row = subjecttable.getSelectedRow();
        stt.setText(subjecttable.getValueAt(row, 0).toString());
        mamh.setText(subjecttable.getValueAt(row, 1).toString());

        tenmh.setText(subjecttable.getValueAt(row, 2).toString());

    }

