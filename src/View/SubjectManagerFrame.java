    private void xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xoaActionPerformed
        // TODO add your handling code here:

        int row = subjecttable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(SubjectManagerFrame.this, "Vui Long Chon Sinh Vien", "Loi", JOptionPane.ERROR_MESSAGE);

        } else {

            int confirm = JOptionPane.showConfirmDialog(SubjectManagerFrame.this, "Ban Chac Chan Muon Xoa?");

            if (confirm == JOptionPane.YES_OPTION) {
                int mamh = Integer.valueOf(String.valueOf(subjecttable.getValueAt(row, 1)));
                subjectController.deleteSubject(mamh);
                defaultTableModel.setRowCount(0);
                setdata(subjectController.getAllSubjects());

                JOptionPane.showMessageDialog(null, "Success");
            }

        }

    }//GEN-LAST:event_xoaActionPerformed

