    private void mamhKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_mamhKeyReleased
        // TODO add your handling code here:

        Connection connection = JDBCConnection.getJDBCConnection();
        String sql = "SELECT * FROM subject WHERE mamh = ?";
        try {
            PreparedStatement preparedStatement = connection.prepareStatement(sql);
            preparedStatement.setString(1, mamh.getText());
            ResultSet rs = preparedStatement.executeQuery();
            if (rs.next()) {
                stt.setText(rs.getString("stt"));

                mamh.setText(rs.getString("mamh"));
                tenmh.setText(rs.getString("tenmh"));

            }

        } catch (SQLException e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_mamhKeyReleased

