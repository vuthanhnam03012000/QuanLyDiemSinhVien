    public void update() {
        subject.setStt(Integer.valueOf(stt.getText()));
        subject.setMaMonHoc(mamh.getText());

        subject.setTenMonHoc(tenmh.getText());

        subjectController.updateSubject(subject);

        for (int i = 0; i <= subjecttable.getRowCount(); i++) {
            if (subjecttable.getValueAt(i, 0).equals(Integer.parseInt(mamh.getText()))) {
                subjecttable.setValueAt(tenmh.getText(), i, 1);
                break;
            }
        }
        JOptionPane.showMessageDialog(null, "Success");
    }

