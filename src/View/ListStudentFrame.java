    public void SelectedRow() {

        int row = studenttable.getSelectedRow();
        id.setText(studenttable.getValueAt(row, 0).toString());
        name.setText(studenttable.getValueAt(row, 1).toString());
        gender.setText(studenttable.getValueAt(row, 2).toString());
        date.setText(studenttable.getValueAt(row, 3).toString());
        lop.setText(studenttable.getValueAt(row, 4).toString());
        address.setText(studenttable.getValueAt(row, 5).toString());

    }

    //Get het du lieu tren database studentinfo vao tablestudent
    private void setdata(List<Student> students) {
        for (Student student : students) {
            defaultTableModel.addRow(new Object[]{
                student.getId(),
                student.getName(),
                student.getGender(),
                student.getDate(),
                student.getLop(),
                student.getAddress()});

        }

    }

