    public void update() {

        studentMark.setId(String.valueOf(idsv.getText()));
        studentMark.setName(tensv.getText());
        studentMark.setTenMonHoc(String.valueOf(combomon.getSelectedItem()));
        studentMark.setDiemChuyenCan(diem15.getText());
        studentMark.setDiemGiuaKy(diem45.getText());
        studentMark.setDiemThi(diemthi.getText());
        studentMark.setDiemTongKet(diemtongket.getText());
        studentMark.setXepLoai(xeploai.getText());
        studentMark.setMaMonHoc(mamh.getText());

        studentMarkController.updateStudentMark(studentMark);

        for (int i = 0; i <= studenttablemark.getRowCount(); i++) {
            if (studenttablemark.getValueAt(i, 0).equals(Integer.parseInt(idsv.getText()))) {
                studenttablemark.setValueAt(tensv.getText(), i, 1);
                break;
            }
        }
        JOptionPane.showMessageDialog(null, "Success");
    }

    public void calculatediem() {

        float diem15a = Float.parseFloat(this.diem15.getText());
        float diem45a = Float.parseFloat((this.diem45.getText()));
        float diemthi1 = Float.parseFloat(this.diemthi.getText());

        // Calculates total marks 
        double diemtongkett = ((0.2 * diem15a) + (0.2 * diem45a) + (0.6 * diemthi1));
        diemtongket.setText("" + diemtongkett);
        if (diemtongkett > 9) {
            xeploai.setText("Xuat Sac");
        } else if ((diemtongkett >= 8) && (diemtongkett <= 9)) {
            xeploai.setText("Gioi");
        } else if ((diemtongkett >= 7) && (diemtongkett < 8)) {
            xeploai.setText("Kha");
        } else if ((diemtongkett >= 6) && (diemtongkett <= 7)) {
            xeploai.setText("Trung Binh");
        } else if ((diemtongkett >= 5) && (diemtongkett < 6)) {
            xeploai.setText("Yeu");
        } else if ((diemtongkett < 5)) {
            xeploai.setText("Kem, Khong Dat");
        }

    }

