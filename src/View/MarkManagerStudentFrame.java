    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:

        int row = studenttablemark.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(MarkManagerStudentFrame.this, "Vui Long Chon Sinh Vien", "Loi", JOptionPane.ERROR_MESSAGE);

        } else {

            int confirm = JOptionPane.showConfirmDialog(MarkManagerStudentFrame.this, "Ban Chac Chan Muon Xoa?");

            if (confirm == JOptionPane.YES_OPTION) {
                int id = Integer.valueOf(String.valueOf(studenttablemark.getValueAt(row, 0)));
                studentMarkController.deleteStudentMark(id);
                defaultTableModel.setRowCount(0);
                setdata(studentMarkController.getAllMark());
                JOptionPane.showMessageDialog(null, "Success");
            }

        }


    }//GEN-LAST:event_btndeleteActionPerformed

